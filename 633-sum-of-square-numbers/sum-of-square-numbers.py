class Solution:
    def judgeSquareSum(self, c: int) -> bool:
            left=0
            right=int(c**0.5)
            while left<=right:
                squares=(left*left)+(right*right)
                if squares==c:
                    return True
                elif squares<c:
                    left+=1
                else:
                    right-=1
           
            return False
        

        

           